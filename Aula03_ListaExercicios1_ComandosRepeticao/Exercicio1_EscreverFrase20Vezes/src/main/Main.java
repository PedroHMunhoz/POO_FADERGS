public class Main {
     /**
     * Exercícios sobre Comandos de Repetição (WHILE, DO...WHILE, FOR)
     * Exercício 1 - Escrever 20 vezes a mensagem 
     * “Eu vou aprender Programação Orientada aObjetos!”.     
     */
    public static void main(String[] args) {
        
        // Faz o loop 20 vezes (contador vai de 0 a 19), imprimindo a frase
        // 20 vezes na tela
        for (int i = 0; i < 20; i++) {
            System.out.println("Eu vou aprender Programação Orientada a Objetos!");            
        }
    }    
}
